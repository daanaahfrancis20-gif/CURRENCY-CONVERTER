export function getCurrencyInfo(code: string): CurrencyInfo {
  const normalized = code.toUpperCase();
  const info = currencyMap.get(normalized);
  if (info) return info;
  
  // Fallback if not found in list
  return {
    code: normalized,
    name: `${normalized} Currency`,
    country: normalized.substring(0, 2).toLowerCase()
  };
}
